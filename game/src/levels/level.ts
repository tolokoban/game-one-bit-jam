export function logLevel(level: number[][], x: number, y: number) {
    console.log(
        `%c${level
            .map((items, row) =>
                items
                    .map((value, col) => {
                        if (col === Math.floor(x) && row === Math.floor(y)) {
                            return value === 1 ? "%c▉%c" : " "
                        }
                        return value === 1 ? "▉" : " "
                    })
                    .join("")
            )
            .join("\n")}`,
        "font-family:monospace;background:#000,color:#ddd",
        "color:#0f0",
        "color:#ddd"
    )
}
